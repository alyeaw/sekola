fun main() {
    println("-------------PENJUMLAHAN-----------");
    println("number1 = 10")
    println("number2 = 25")

    var number1: Int = 10
    var number2: Int = 25
    var tambah: Int = number1+number2

    println("number1 + number2 = ${tambah}")


    println("--------------PENGURANGAN-------------")
    println("a = 75")
    println("b = 55");

    var a: Int = 75
    var b: Int = 55
    var kurang: Int = a-b

    println("a - b = ${kurang}")


    println("--------------PEMBAGIAN-------------")
    println("x = 128")
    println("y = 7");

    var x: Int = 128
    var y: Int = 7
    var pembagian: Int = x/y

    println("a - b = ${pembagian}")


    println("--------------PERKALIAN-------------")
    println("F = 56")
    println("S = 40");

    var F: Int = 56
    var S: Int = 40
    var perkalian: Int = F*S

    println("a - b = ${perkalian}")



}
